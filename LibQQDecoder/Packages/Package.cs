using LibAsyncUDP.Interfaces;
using System;

namespace LibQQDecoder.Packages {
    public abstract class Package : IPackage {
        protected static Byte[] Head = { 0x02 };
        protected static Byte[] Version = { 0x37, 0x13 };
        protected static Byte[] UnknowData01 = { 0x03, 0x00, 0x00, 0x00, 0x01, 0x2E, 0x01, 0x00, 0x00, 0x68, 0x52, 0x00, 0x00, 0x00, 0x00 };
        protected static Byte[] PublicKey = { 0x02, 0x6D, 0x28, 0x41, 0xD2, 0xA5, 0x6F, 0xD2, 0xFC, 0x3E, 0x2A, 0x1F, 0x03, 0x75, 0xDE, 0x6E, 0x28, 0x8F, 0xA8, 0x19, 0x3E, 0x5F, 0x16, 0x49, 0xD3 };
        protected static Byte[] Tail = { 0x03 };


        protected Byte[] _PackageData { get; set; } = new Byte[] { };
        protected Int32 _DataLength => this._PackageData.Length;

        public abstract Byte[] PackageData { get; set; }
        public abstract Int32 DataLength { get; }
    }
}
